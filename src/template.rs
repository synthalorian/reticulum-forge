//! Template engine wrapper for Reticulum config generation.
//!
//! # Security
//! - All templates are embedded (no filesystem loading) — prevents template injection via file path.
//! - Tera auto-escapes output by default — prevents injection through rendered values.
//! - Template names are validated against a known set — no arbitrary template lookups.

use crate::error::{ForgeError, ForgeResult};

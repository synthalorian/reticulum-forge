_forge() {
    local i cur prev opts cmd
    COMPREPLY=()
    if [[ "${BASH_VERSINFO[0]}" -ge 4 ]]; then
        cur="$2"
    else
        cur="${COMP_WORDS[COMP_CWORD]}"
    fi
    prev="$3"
    cmd=""
    opts=""

    for i in "${COMP_WORDS[@]:0:COMP_CWORD}"
    do
        case "${cmd},${i}" in
            ",$1")
                cmd="forge"
                ;;
            forge,completions)
                cmd="forge__subcmd__completions"
                ;;
            forge,deploy)
                cmd="forge__subcmd__deploy"
                ;;
            forge,generate)
                cmd="forge__subcmd__generate"
                ;;
            forge,help)
                cmd="forge__subcmd__help"
                ;;
            forge,init)
                cmd="forge__subcmd__init"
                ;;
            forge,man)
                cmd="forge__subcmd__man"
                ;;
            forge,monitor)
                cmd="forge__subcmd__monitor"
                ;;
            forge,simulate)
                cmd="forge__subcmd__simulate"
                ;;
            forge,test)
                cmd="forge__subcmd__test"
                ;;
            forge__subcmd__help,completions)
                cmd="forge__subcmd__help__subcmd__completions"
                ;;
            forge__subcmd__help,deploy)
                cmd="forge__subcmd__help__subcmd__deploy"
                ;;
            forge__subcmd__help,generate)
                cmd="forge__subcmd__help__subcmd__generate"
                ;;
            forge__subcmd__help,help)
                cmd="forge__subcmd__help__subcmd__help"
                ;;
